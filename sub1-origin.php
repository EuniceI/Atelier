        <section class="at1">
            <h6 class="ath1">The origins of the earliest constellations </h6>
            <p>likely go back to prehistory. People used them to relate stories of their beliefs, experiences, creation, or mythology. Different cultures and countries adopted their own constellations, some of which lasted into the early 20th century before today's constellations were internationally recognized. The recognition of constellations has changed significantly over time. Many changed in size or shape. Some became popular, only to drop into obscurity. Some were limited to a single culture or nation.</p>
        </section>
        <section class="at2">
            <h6 class="ath2">The 48 traditional Western constellations</h6>
            <p>are Greek. They are given in Aratus' work Phenomena and Ptolemy's Almagest, though their origin probably predates these works by several centuries. Constellations in the far southern sky were added from the 15th century until the mid-18th century when European explorers began traveling to the Southern Hemisphere. Twelve ancient constellations belong to the zodiac (straddling the ecliptic, which the Sun, Moon, and planets all traverse). The origins of the zodiac remain historically uncertain; its astrological divisions became prominent c. 400 BC in Babylonian or Chaldean astronomy.</p>
        </section>
        <section class="at3">
            <h6 class="ath3">In 1922,</h6>
            <p>the International Astronomical Union (IAU) formally accepted the modern list of 88 constellations, and in 1928 adopted official constellation boundaries that together cover the entire celestial sphere. Any given point in a celestial coordinate system lies in one of the modern constellations. Some astronomical naming systems include the constellation where a given celestial object is found to convey its approximate location in the sky. The Flamsteed designation of a star, for example, consists of a number and the genitive form of the constellation name.</p>
        </section>
        <section class="at4">
            <h6 class="ath4">ASTERISM</h6>
            <p>Other star patterns or groups called asterisms are not constellations under the formal definition, but are also used by observers to navigate the night sky. Asterisms may be several stars within a constellation, or they may share stars with more than one  constellation. Examples of asterisms include the Pleiades and Hyades within the constellation Taurus and the False Cross split  between the southern constellations Carina and Vela, or Venus' Mirror in the constellation of Orion.</p>
